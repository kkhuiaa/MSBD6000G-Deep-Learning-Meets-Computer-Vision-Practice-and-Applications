  correct_classs = score[range(0, num_train), y]

  #reshape it for next step
  correct_classs = np.array([correct_classs, ]*num_classes)

  #calculate the loss
  softmax_loss = np.maximum(0, score - correct_classs.T + 1) #1 is to still penalize it if the gap is small
  softmax_loss[range(0, num_train), y] = 0 #the correct class should have zero loss
  loss = np.sum(softmax_loss)/num_train + reg*np.sum(W*W) #calculate the loss accordingly

